import {
  actions,
  reducer
} from 'routes/ChoiceAs/modules/choiceas'

it('Should increment the state by the action payload\'s property.', () => {
    let state = reducer(undefined, {})
    expect(state).to.equal(0)
    state = reducer(state, actions.keyClick(1))
    expect(state).to.equal(1)
    state = reducer(state, actions.keyClick(2))
    expect(state).to.equal(3)
    state = reducer(state, actions.keyClick(-3))
    expect(state).to.equal(0)
})
